<p>Саратов был основан в первых числах июля 1590 года как сторожевая крепость для охраны южных рубежей Российского государства. Дело в том, что после походов русских войск на Казанское ханство и на Астраханское ханство Русское царство получило обширные территории Поволжья. Было решено строить на Волге новые города-крепости. Летом 1586 года была основана крепость Самара, а летом 1589 года — Царицын (ныне Волгоград). Саратов основали князь Григорий Засекин и боярин Федор Туров на полпути между Самарой и Царицыном.
    Сегодня мы вспоминаем основные вехи истории Саратова — крупного культурного, экономического и образовательного центра Поволжья.
    </p>
