<h1>Памятник П. А. Столыпину</h1>
<p>Памятник Столыпину — скульптурная композиция, посвященная российскому реформатору, премьер-министру и саратовскому губернатору Петру Столыпину; расположена в Саратове на площади Столыпина. Он был открыт 17 апреля 2002 года в рамках торжеств, посвящённых 140-летию со дня рождения Петра Столыпина.

    Как отмечали современники, П. А. Столыпин оказал Саратову значительные услуги, главная из которых — открытие в Саратове университета в 1909 году. За три года губернаторства Столыпина в Саратове закладывается здание Мариинской женской гимназии (ныне — здание авиационного колледжа), асфальтируются улицы Никольская (ныне Радищева) и Александровская (ныне Максима Горького). Губернатор добивается для города громадного займа в 965 тысяч рублей на устройство водопровода и мостовых. Начинается модернизация городской телефонной сети, состоялись пробные пуски газового освещения. Открываются приюты, ночлежные дома.
    
    Памятник располагается перед Саратовской городской думой. В честь него получила название площадь, ранее безымянная. Автор скульптуры — Вячеслав Клыков.Совместно со старшим братом Игоря Талькова — Владимиром Владимировичем Тальковым
    
    Монумент представляет собой 3,5-метровую фигуру российского премьер-министра. Столыпин запечатлён в форменном кителе; на четырёхметровом пьедестале выбит знаменитый столыпинский девиз «Нам нужна великая Россия!».
    
    Окружают памятник фигуры крестьянина, священника, кузнеца и воина. Подобная идея применялась в памятнике Александру II (только у него набор фигур был другим). Эти фигуры символизируют традиционность русской культуры и её возможность гармонично развиваться в любых исторических условиях. Памятник обращён лицом на улицу Радищева, наискосок от памятника находится Радищевский музей.</p>
