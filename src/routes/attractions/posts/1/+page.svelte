<h1>Мемориальный комплекс Журавли</h1>
<p>В 1977 году в Саратове объявили конкурс на лучший проект памятника землякам, погибшим на фронтах Великой Отечественной войны. Победил проект архитектора Юрия Менякина.
    Замысел и архитектурное решение Мемориала навеяны песней «Журавли» на стихи Расула Гамзатова.Впоследствии при личной встрече Менякина с Гамзатовым в Москве поэт сказал, что именно таким он представлял материальное воплощение своего стихотворения. Основная тема Мемориала — светлая память и светлая печаль.
    Строительство Мемориала началось весной 1980 года и закончилось в апреле 1982 г.
    Из воспоминаний Ольги Менякиной: «Когда проектировались „Журавли“, наша квартира превратилась в скульптурную мастерскую. Сначала Юрий Иванович отливал фигурки журавлей из олова, постепенно увеличивая размеры, экспериментировал с пропорциями.
    Рассматривалось несколько точек установки Мемориала на Соколовой горе. Проводились геологические исследования грунта, при наличии оползневых зон определялись степень безопасности сооружения и зависимости его сохранности от климатических природных условий, подбор строительных материалов. После окончательного утверждения места установки начались строительные работы. Для удобства посетителей Мемориала рассчитывались углы подъёма и наклонов склона холма, ширина и высота парапета и ступеней лестницы. Для отдыха после восхождения на смотровую площадку и к основанию пилонов вдоль растекающихся от подножия холма пешеходных дорожек под кроной деревьев установлены лавочки…».Возвышенный природный рельеф местности — Соколовая гора — при сооружении Мемориала использован архитектором для его максимального обозрения жителями города. Для возвеличивания монумента на Соколовой горе был сооружён рукотворный Холм — как составная часть Мемориала. Холм в виде усечённого конуса состоят из забитых железобетонных свай и земли, уплотнённой виброплощадками. Холм покрыт газонной травой и кустарником.
    Для внешнего оформления Мемориала, кроме смальты и брусчатки, были использованы материалы местного производства. При строительстве и изготовлении элементов применены новейшие и уникальные по тем временам технологии. Все элементы Мемориала от его начала — Мемориального камня — и до вершины Холма расположены по одной восходящей линии.</p>

