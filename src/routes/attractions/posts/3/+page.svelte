<h1>Свято-Троицкий собор</h1>
<p>Троицкий собор является старейшим сооружением города Саратова. Заложен он стрельцами в 1689 году, на месте деревянного храма. Постройка собора была завершена лишь к 1695 году, а в 1723 году – завершена окончательная отделка храма. 

    Собор возведен в стиле русской архитектуры 16 – начала 17 вв., архитектор неизвестен. Построен храм в стиле московского или «нарышкинского» барокко. Это течение архитектуры, обильно декорированное, изукрашенное пришлось по вкусу купцам и ремесленникам торговых городов того времени. Высота собора с крестом около 35 метров, в окружности – 95 метров. 
    
    За всю свою историю Троицкий собор много раз страдал от пожаров: в 1712, 1757, 1801 и других годах. После особенно сильного пожара 1774 года, было принято решения о сносе здания. Но собор уцелел. Проект капитальной реконструкции был подготовлен петербургскими специалистами И.Ф.Колодиным и В.А.Фохтом в 1813 – 1814 гг. Проект, правда, в более упрощенном виде реализовывали саратовский архитектор В.И.Суранов и тамбовский архитектор Н.В.Урюпин. 
    
    12 сентября 1814 года в соборе было возобновлено богослужение. В 1826 году с постройкой Александро-Невского собора Троицкий собор стал называться старым, и был обращен в одноштатную приходскую церковь. В 1872 году вокруг собора была устроена ограда. В 1904 году был произведен новый ремонт собора, и переделаны боковые входы по проекту архитектора В.А. Люкшина. 
    
    В середине 1930-х годов собор был закрыт и превращен в граверную мастерскую, а потом в склад военного имущества. В 1942 году собор вновь открыли под давлением общественного мнения в связи с начавшейся Великой Отечественной войной, а в 1947 году окончательно передали Русской Православной церкви. 
    
    В 1972-1976 гг. по проекту и под личным руководством ленинградского архитектора Д.Е. Бабенкова были произведены реставрационные работы, золочение крестов, куполов и подзоров, обновление здания собора, обновление росписей в верхних и нижних церквях. В 1988 году в Свято-Троицком Соборе состоялись торжества, посвященные 1000-летию крещения Руси. В 1989 году был возобновлен колокольный звон, а в 1993 году собор посетил Святейший Патриарх московский и всея Руси Алексий II.
    
    Свято-Троицкий собор - древнейший хранитель православной духовной традиции. Он помнит выдающихся иерархов Русской церкви, чьи имена тесно связаны с историей Саратова и России. Среди них епископ Иаков (Вечерков), епископ Гермоген (Долганов), архиепископ Пимен (Хмелевский). В 1722 году собор посетил Петр I, направлявшийся по Волге в Персидский поход. В 1809 году Александр I подарил собору крещатую ризу. 
    
    В алтаре собора хранится чудотворная икона «Спас Нерукотворный», привезенная ещё первыми поселенцами Саратова на заклад города. По преданию, икона является одним из списков подобной иконы, кисти Андрея Рублева. 
    
    В течение нескольких веков складывалась выразительная и сложная композиция Троицкого собора: изменялась в деталях, дополнялась новыми элементами, но при этом не утратила своей целостности. Вначале стоявшие отдельно храм и восьмигранная шатровая колокольня, со временем были объедены крытой галерей и трапезной. 
    
    Легкая, устремленная в небеса колокольня остро контрастирует с приземистым основанием храма. Собор, который был существенно расширен благодаря трапезной, обрамляет галерея на мощных арках с контрфорсами. Она словно связывает постройку с землей. 
    
    Декоративность – отличительная черта собора Святой Троицы. Все его элементы, словно взывают к радостям мирской жизни. Старый собор в своё время стал центром одноименной площади – Старой Соборной.</p>

